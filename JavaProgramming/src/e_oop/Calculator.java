package e_oop;

public class Calculator {
	
	//더하기
	double add(double x, double y){
		return x + y;
	}
	
	//빼기
	double subtract(double x, double y){
		return x - y;
	}
	
	//곱하기
	double multiply(double x, double y){
		return x * y;
	}
	
	//나누기
	double divide(double x, double y){
		return x / y;
	}
	//나머지 
	double mod(double x, double y){
		return x % y;
	}

}
